// Get current language from localStorage or default to English
let currentLang = localStorage.getItem('language') || 'en';

/**
 * Get translation for a key
 * @param {string} key - The translation key
 * @param {string} lang - Optional language override
 * @returns {string} The translated text
 */
function translate(key, lang = null) {
    const language = lang || currentLang;
    return bihakExtendedTranslations[language]?.[key] || key;
}

// Shorthand function
const t = translate;

/**
 * Get all translations for current language
 */
function getAllTranslations(lang = null) {
    const language = lang || currentLang;
    return bihakExtendedTranslations[language] || bihakExtendedTranslations.en;
}

/**
 * Switch language and update entire page
 * @param {string} lang - Language code (en/fr)
 */
function switchLanguage(lang) {
    if (!bihakExtendedTranslations[lang]) {
        console.error(`Language '${lang}' not supported`);
        return;
    }

    currentLang = lang;
    localStorage.setItem('language', lang);

    // Update language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    const langBtn = document.getElementById('lang-' + lang);
    if (langBtn) langBtn.classList.add('active');

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update all translatable elements
    translatePage(lang);

    // Dispatch language change event
    document.dispatchEvent(new CustomEvent('languageChanged', {
        detail: {
            language: lang,
            translations: getAllTranslations(lang),
            t: (key) => translate(key, lang)
        }
    }));
}

/**
 * Translate entire page based on data-translate attributes
 */
function translatePage(lang) {
    const translations = getAllTranslations(lang);

    // Update navigation
    updateNavigation(translations);

    // Update elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[key]) {
            // Handle different element types
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (element.placeholder) {
                    element.placeholder = translations[key];
                }
                if (element.value && element.hasAttribute('data-translate-value')) {
                    element.value = translations[key];
                }
            } else if (element.tagName === 'BUTTON' || element.tagName === 'A') {
                // Preserve icons and other elements, only change text
                const textNode = Array.from(element.childNodes).find(node => node.nodeType === Node.TEXT_NODE);
                if (textNode) {
                    textNode.textContent = translations[key];
                } else {
                    element.textContent = translations[key];
                }
            } else {
                element.textContent = translations[key];
            }
        }
    });

    // Update titles and aria-labels
    document.querySelectorAll('[data-translate-title]').forEach(element => {
        const key = element.getAttribute('data-translate-title');
        if (translations[key]) {
            element.title = translations[key];
        }
    });

    document.querySelectorAll('[data-translate-aria]').forEach(element => {
        const key = element.getAttribute('data-translate-aria');
        if (translations[key]) {
            element.setAttribute('aria-label', translations[key]);
        }
    });
}

/**
 * Update navigation menu
 */
function updateNavigation(translations) {
    const navLinks = document.querySelectorAll('.nav-links a');
    navLinks.forEach(link => {
        const href = link.getAttribute('href');
        if (href) {
            if (href.includes('index')) link.textContent = translations.home;
            else if (href.includes('about')) link.textContent = translations.about;
            else if (href.includes('stories')) link.textContent = translations.stories;
            else if (href.includes('work')) link.textContent = translations.work;
            else if (href.includes('opportunities')) link.textContent = translations.opportunities;
            else if (href.includes('contact')) link.textContent = translations.contact;
        }
    });

    // Update header buttons
    const incubationBtn = document.querySelector('.btn-incubation span');
    if (incubationBtn) incubationBtn.textContent = translations.incubation;

    const getInvolvedBtn = document.querySelector('.btn-get-involved span');
    if (getInvolvedBtn) getInvolvedBtn.textContent = translations.getInvolved;

    const shareBtn = document.querySelector('.btn-share-story span');
    if (shareBtn) shareBtn.textContent = translations.shareStory;

    const loginBtn = document.querySelector('.btn-login span');
    if (loginBtn) loginBtn.textContent = translations.login;

    // Update user dropdown
    const accountLink = document.querySelector('a[href*="my-account"]');
    if (accountLink) {
        const textNode = Array.from(accountLink.childNodes).find(n => n.nodeType === Node.TEXT_NODE);
        if (textNode) textNode.textContent = ' ' + translations.myAccount;
    }

    const profileLink = document.querySelector('a[href*="profile"]');
    if (profileLink) {
        const textNode = Array.from(profileLink.childNodes).find(n => n.nodeType === Node.TEXT_NODE);
        if (textNode) textNode.textContent = ' ' + translations.myProfile;
    }

    const logoutLink = document.querySelector('a[href*="logout"]');
    if (logoutLink) {
        const textNode = Array.from(logoutLink.childNodes).find(n => n.nodeType === Node.TEXT_NODE);
        if (textNode) textNode.textContent = ' ' + translations.logout;
    }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    // Get saved language or default to English
    const savedLang = localStorage.getItem('language') || 'en';
    switchLanguage(savedLang);
});

